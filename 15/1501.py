#使用turtle库中的turtle.fd()函数和turtle.seth()函数绘制一个叠加等边三角形
import turtle
turtle.setup(600,600,200,200)
turtle.fd(200)
turtle.seth(120)
turtle.fd(200)
turtle.seth(240)
turtle.fd(100)
turtle.seth(0)
turtle.fd(100)
turtle.seth(240)
turtle.fd(100)
turtle.seth(120)
turtle.fd(100)
turtle.seth(240)
turtle.fd(100)
