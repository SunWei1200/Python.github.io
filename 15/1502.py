#利用turtle库绘制一个六角形
import turtle
turtle.setup(600,600,200,200)
turtle.seth(30)
turtle.fd(180)
turtle.seth(-90)
turtle.fd(180)
turtle.seth(150)
turtle.fd(180)
turtle.seth(30)
turtle.fd(60)
turtle.seth(90)
turtle.fd(60)
turtle.seth(-30)
turtle.fd(180)
turtle.seth(-150)
turtle.fd(180)
turtle.seth(90)
turtle.fd(120)
